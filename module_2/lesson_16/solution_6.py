"""–û–ø—Ç–∏–º–∏–∑–∞—Ü–∏—è —Ä–∞–±–æ—á–∏—Ö –ø—Ä–æ—Å—Ç—Ä–∞–Ω—Å—Ç–≤"""

def matrix_gen(size):
    """
    –ì–µ–Ω–µ—Ä–∏—Ä—É–µ—Ç –º–∞—Ç—Ä–∏—Ü—É –∏—Å—Ö–æ–¥—è –∏–∑ –∑–∞–¥–∞–Ω–Ω—ã—Ö –ø–∞—Ä–∞–º–µ—Ç—Ä–æ–≤

    args:
        size (int): —Ä–∞–∑–º–µ—Ä –º–∞—Ç—Ä–∏—Ü–∏

    return:
        matrix (list): 
    """
    matrix = [["*"] * size for _ in range(size)]
    
    # –∑–∞–ø–æ–ª–Ω—è–µ–º –Ω—É–ª—è–º–∏ –æ—Ç –≤–µ—Ä—Ö–Ω–µ–≥–æ –ª–µ–≤–æ–≥–æ –¥–æ –Ω–∏–∂–Ω–µ–≥–æ –ø—Ä–∞–≤–æ–≥–æ —É–≥–ª–∞
    for i in range(size):
        matrix[i][i] = 0

    # –∑–∞–ø–æ–ª–Ω—è–µ–º –æ—Ç –ø—Ä–∞–≤–æ–≥–æ –≤–µ—Ä—Ö–Ω–µ–≥–æ –¥–æ –ª–µ–≤–æ–≥–æ –Ω–∏–∂–Ω–µ–≥–æ —É–≥–ª–∞
    for i in range(size):
        if i != size // 2: # –∏—Å–∫–ª—é—á–∞–µ–º —Ü–µ–Ω—Ç—Ä–∞–ª—å–Ω–æ–µ –º–µ—Å—Ç–æ
            matrix[i][size - 1 - i] = 5

    # –∑–∞–ø–æ–ª–Ω—è–µ–º –≤–µ—Ä—Ö–Ω—é—é —á–∞—Å—Ç—å –∫–≤–∞–¥—Ä–∞—Ç–∞
    for i in range(size):
        for j in range(i + 1, size - 1 - i):
            matrix[i][j] = 1

    # –∑–∞–ø–æ–ª–Ω—è–µ–º –ª–µ–≤—É—é —á–∞—Å—Ç—å –∫–≤–∞–¥—Ä–∞—Ç–∞
    for i in range(size):
        for j in range(i + 1, size - 1 - i):
            matrix[j][i] = 2

    # –∑–∞–ø–æ–ª–Ω—è–µ–º –Ω–∏–∂–Ω—é—é —á–∞—Å—Ç—å –∫–≤–∞–¥—Ä–∞—Ç–∞ 
    for i in range(size - 1, 0, -1):
        for j in range(size - i, i):
            matrix[i][j] = 3


    # –∑–∞–ø–æ–ª–Ω—è–µ—Å –ø—Ä–∞–≤—É—é —á–∞—Å—Ç—å –∫–≤–∞–¥—Ä–∞—Ç–∞
    for i in range(size - 1, 0, -1):
        for j in range(size - i, i):
            matrix[j][i] = 4        
    
    return matrix

