def spiral_matrix_gen(side):
    """
    –ì–µ–Ω–µ—Ä–∏—Ä—É–µ—Ç —Å–ø–∏—Ä–∞–ª—å–Ω—É—é –º–∞—Ç—Ä–∏—Ü—É

    args:
        side (int): –¥–ª–∏–Ω–Ω–∞ —Å—Ç–æ—Ä–æ–Ω —Å–ø–∏—Ä–∞–ª–∏
    
    return:
        matrix (list): —Å–ø–∏—Ä–∞–ª—å–Ω–∞—è –º–∞—Ç—Ä–∏—Ü–∞
    """
    matrix = [[0] * side for i in range(side)]

    # –∏–Ω–∏—Ü–∏–∞–ª–∏–∑–∞—Ü–∏—è –ø–µ—Ä–µ–º–µ–Ω–Ω—ã—Ö
    top, left, bottom, right, step = 0, 0, side - 1, side - 1, 1

    while top <= bottom and left <= right:
        # –∑–∞–ø–æ–ª–Ω–µ–Ω–∏–µ –≤–µ—Ä—Ö–Ω–µ–≥–æ —Ä—è–¥–∞
        for i in range(left, right + 1):
            matrix[top][i] = step
            step += 1
        top += 1

        # –∑–∞–ø–æ–ª–Ω–µ–Ω–∏–µ –ø—Ä–∞–≤–æ–≥–æ —Å—Ç–æ–ª–±—Ü–∞
        for i in range(top, bottom + 1):
            matrix[i][right] = step
            step += 1
        right -= 1

        # –∑–∞–ø–æ–ª–Ω–µ–Ω–∏–µ –Ω–∏–∂–Ω–µ–≥–æ —Ä—è–¥–∞
        if top <= bottom:
            for i in range(right, left - 1, -1):
                matrix[bottom][i] = step
                step += 1
            bottom -= 1

        # –∑–∞–ø–æ–ª–Ω–µ–Ω–∏–µ –ª–µ–≤–æ–≥–æ —Å—Ç–æ–ª–±—Ü–∞
        if left <= right:
            for i in range(bottom, top - 1, -1):
                matrix[i][left] = step
                step += 1
            left += 1
        
    return matrix


def spiral_matrix_print(matrix):
    """
    –§—É–Ω–∫—Ü–∏—è –≤—ã–≤–æ–¥–∏—Ç –º–∞—Ç—Ä–∏—Ü—É
    """
    max_num_length = len(str(max(max(matrix)))) + 1  # –Ω–∞—Ö–æ–¥–∏–º –¥–ª–∏–Ω—É —Å–∞–º–æ–≥–æ –¥–ª–∏–Ω–Ω–æ–≥–æ —á–∏—Å–ª–∞ –≤ –º–∞—Ç—Ä–∏—Ü–µ
    print("–ú–∞—Ç—Ä–∏—Ü–∞ –ø—Ä–æ—ç–∫—Ç–æ–≤:")
    
    for row in matrix:
        formatted_row = [str(num).rjust(max_num_length) for num in row]
        print(" ".join(formatted_row))



